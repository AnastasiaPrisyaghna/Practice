fn main() {
    let mut sum = 0;
    for i in -3..2 {
        sum += i;
    }

    // Виправлення: сума повинна бути -3, бо ми додаємо числа -3, -2, -1, 0, 1
    assert!(sum == -3);

    // Виведення всіх символів від 'a' до 'z'
    for c in 'a'..='z' {
        println!("{}", c); // Виведе всі літери від 'a' до 'z'
    }
}
